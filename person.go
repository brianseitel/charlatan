var listTitleMale = []string{"Mr.", "Dr.", "Prof."}
var listTitleFemale = []string{"Mrs.", "Ms.", "Miss", "Dr.", "Prof."}

func firstName() interface{} {
	if goTime.Now().UnixNano()%2 == 0 {
		return strings.Title(maleName().(string))
	}

	return strings.Title(femaleName().(string))
}

func maleName() interface{} {
	return randomElement(firstNameMale)
}

func femaleName() interface{} {
	return randomElement(firstNameMale)
}

func lastName() interface{} {
	return strings.Title(randomElement(lastNames))
}

func fullName() interface{} {
	return strings.Title(fmt.Sprintf("%s %s", firstName(), lastName()))
}

func suffix() interface{} {
	return randomElement(listSuffix)
}

func title() interface{} {
	if goTime.Now().UnixNano()%2 == 0 {
		return fmt.Sprintf("%s %s %s", titleFemale(), femaleName(), lastName())
	}
	return fmt.Sprintf("%s %s %s", titleMale(), maleName(), lastName())
}

func titleFemale() interface{} {
	return randomElement(listTitleFemale)
}

func titleMale() interface{} {
	return randomElement(listTitleMale)
}

